typedef enum {
    CODE_SUCCESS = 0,
    ERROR_WRONG_NUMBER_OF_ARGUMENTS = 1,
    ERROR_FAILED_TO_OPEN_OUTPUT_FILES,
    ERROR_FAILED_TO_OPEN_SPV_FILE,
    FAILED_TO_DETECT_SHADER_TYPE
} ReturnCodes;

